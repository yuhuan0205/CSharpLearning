namespace LittleComputer
{
    /// <summary>
    /// a class contains all const variable.
    /// </summary>
    public class Consts
    {
        /// <summary>
        /// zero
        /// </summary>
        public const int ZERO = 0;

        /// <summary>
        /// first index
        /// </summary>
        public const int FIRST_INDEX = 0;

        /// <summary>
        /// EmptyString
        /// </summary>
        public const string EMPTY_STRING = "";

        /// <summary>
        /// ZeroString
        /// </summary>
        public const string ZERO_STRING = "0";

        /// <summary>
        /// EqualSign
        /// </summary>
        public const string EQUAL_SIGN = "=";

        /// <summary>
        /// PlausSign
        /// </summary>
        public const string PLAUS_SIGN = "+";

        /// <summary>
        /// MinusSign
        /// </summary>
        public const string MIMUS_SIGN = "-";

        /// <summary>
        /// MultiplySign
        /// </summary>
        public const string MULTIPLY_SIGN = "*";

        /// <summary>
        /// DivideSign
        /// </summary>
        public const string DIVIDE_SIGN = "/";

        /// <summary>
        /// Point
        /// </summary>
        public const string POINT = ".";

        /// <summary>
        /// a const int represent False Condition.
        /// </summary>
        public const int FALSE = 0;

        /// <summary>
        /// a const int represent True Condition.
        /// </summary>
        public const int TRUE = 1;

        /// <summary>
        /// a const int to lock zero typed in.
        /// </summary>
        public const int LOCKED = 0;

        /// <summary>
        /// a const int to unlock zero typed in.
        /// </summary>
        public const int UNLOCKED = -1;
    }
}
